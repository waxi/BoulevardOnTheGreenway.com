                <p>Despite being one of the oldest and longest-inhabited cities in the country, Boston has managed to constantly reinvent and improve itself to stay on track with modern times. Its beloved Rose Kennedy Greenway is perhaps one of the best examples of this phenomenon. In the scheme of the city’s history, however, the Greenway is a definite newcomer. Even so, in the barely more than a decade of its existence, the lovely linear park has become one of the most cherished areas in the city. Read on to learn more about the Greenway and how it came to be.</p>

                <p>Covering 17 acres of land, the Rose Kennedy Greenway stretches more than a mile through prime areas of Chinatown, the Financial District, the Waterfront and the North End. The latter is where its namesake, Rose Fitzgerald Kennedy – the mother of President John F. Kennedy – was born. Featuring tinkling fountains, gorgeously manicured gardens, elegant promenades, charming plazas and a variety of permanent and rotating art exhibitions, the Greenway in its current incarnation officially opened in October 2008. However, its history stretches much farther back than that; indeed, the first inklings of what would become the Greenway of today began all the way back in the 1940s.</p>

                <p>Leading up to the 1940s, which included a huge surge in the number of vehicles on local roads, city officials started looking for ways to alleviate congestion around downtown Boston and to create a direct route that would make it easier to ferry goods into and out of downtown Boston. The “Highway of the Skies,” an elevated artery, was proposed around that time. Eventually called the Central Artery, construction began in 1951 and was completed in 1959. For the elevated freeway to be made possible, more than 1,000 buildings were demolished, and more than 10,000 residents were displaced.</p>

                <p>Although the Central Artery probably helped with matters of congestion and the like initially, city planners soon realized that its design imposed serious limitations on how that part of the city could be developed and laid out for maximum efficiency. After more than a decade of intense planning, construction officially began on the Central Artery/Tunnel Project – better known as the Big Dig – in 1991. The primary goal of the project was to move the elevated highway underground, which would free up much-needed and highly valuable land in one of the most in-demand areas of the city.</p>

                <p>Even before the Big Dig was close to completion, many in the city began envisioning the ways in which the affected neighborhoods would benefit from all of the newfound opened up space. Visions of a series of gardens and parks that would connect neighborhoods like Chinatown, the Waterfront and the North End began to emerge. Finally, the concept behind the Greenway came into being as a joint effort between the Massachusetts Turnpike Authority, or MTA; the Commonwealth of Massachusetts; the City of Boston; and a variety of civic groups.</p>

                <p>Tens of thousands of people showed up for the Inaugural Celebration on October 4, 2008. Put together by the Rose Kennedy Greenway Conservancy, which was established in 2004 as a non-profit to raise funds and to direct the emerging park system, the event enjoyed a great deal of fanfare. The Greenway has only been improved since then, with parks dotting its length. More than 400 free events are held along the Greenway every year, and free Wi-Fi has been available along its length since 2010. The park’s popularity has soared from one year to the next. In 2009, it received around 96,000 visitors; by 2016, that figure had swelled to more than 1.38 million. If you would like to find a home on or near the Greenway, Boston City Properties can help. Contact us today for more information.</p>  

